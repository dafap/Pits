<?php
/**
* Filtre pour supprimer les accents dans une chaine
*
* @project télé-paiement CCM
* @package application/forms/filters
* @author DAFAP Informatique - Alain Pomirol (dafap@free.fr)
* @date 23 mai 2011 - rev. 19/09/2011
